// Calculate stats from mock data
export function calculatePermitStats(permits: PermitMapItem[]) {
  const stats = {
    total: permits.length,
    totalValue: 0,
    byType: {
      new_construction: 0,
      demolition: 0,
      commercial_buildout: 0,
      major_renovation: 0,
      multifamily: 0,
      other: 0,
    } as Record<PermitType, number>,
  }

  for (const permit of permits) {
    stats.byType[permit.permit_type]++
    if (permit.project_value) {
      stats.totalValue += permit.project_value
    }
  }

  return stats
}
